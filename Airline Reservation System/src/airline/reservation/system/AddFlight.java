    private void AutoId(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/airlinereservationsystem", "root", "TULera@123");
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("Select MAX(FlightId) from flight");
            rs.next();
            
            if(rs.getString("MAX(FlightId)")==null)
            {
                flightid.setText("FL001");
            }
            else{
                long id = Long.valueOf(rs.getString("MAX(FlightId)").substring(2));
                id++;
                flightid.setText("FL" + String.format("%03d", id));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LoginWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

