    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            if(JOptionPane.showConfirmDialog( null, "Confirm if you want to abort your journey.", "Alert !",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_NO_OPTION)
            {
                String TICKETID = ticketid.getText();
                String CUSTOMERID = customerid.getText();
                String FLIGHTID = flightid.getText();
            
                Class.forName("com.mysql.jdbc.Driver");
                con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/airlinereservationsystem", "root", "TULera@123");
                pre = con.prepareStatement("select Tickets from ticket where TicketId=?");
                pre.setString(1, TICKETID);
                ResultSet rs = pre.executeQuery();
                rs.next();
            
                int n = Integer.parseInt(rs.getString("Tickets"));
                
                pre = con.prepareStatement("delete from ticket where TicketId=? and CustomerId=? and FlightId=?");
                pre.setString(1, TICKETID);
                pre.setString(2, CUSTOMERID);
                pre.setString(3, FLIGHTID);
                pre.executeUpdate();
             
                //No. of seats increases in flight table as per abort the journey.
                pre = con.prepareStatement("select Seats from flight where FlightId=?");
                pre.setString(1, FLIGHTID);
                rs = pre.executeQuery();
                rs.next();
            
                int m = Integer.parseInt(rs.getString("Seats"));
                String RESULT = String.valueOf(n+m);
            
                pre = con.prepareStatement("update flight set Seats=? where FlightId=?");
                pre.setString(1, RESULT);
                pre.setString(2, FLIGHTID);
                pre.executeUpdate();
            
                JOptionPane.showMessageDialog(null, "Journey aborted successfully, \n 70% of the amount will be refunded to you.");
                ticketid.setText("");
                customerid.setText("");
                flightid.setText("");
                flightname.setText("");
                departure.setText("");
                arrival.setText("");
                duration.setText("");
                dob.setDate(null);
                fare.setText("");
                tickets.setText("");
                total.setText("");
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AbortJourney.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AbortJourney.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


