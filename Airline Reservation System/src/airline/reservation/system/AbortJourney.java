    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String TICKETID = ticketid.getText();
            String CUSTOMERID = customerid.getText();
            String FLIGHTID = flightid.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/airlinereservationsystem", "root", "TULera@123");
            pre = con.prepareStatement("select * from ticket where TicketId=? and CustomerId=? and FlightId=?");
            pre.setString(1, TICKETID);
            pre.setString(2, CUSTOMERID);
            pre.setString(3, FLIGHTID);
            
            ResultSet rs = pre.executeQuery();
            if(rs.next()==false){
                JOptionPane.showMessageDialog(null, "Input data is not valid");
                ticketid.setText("");
                customerid.setText("");
                flightid.setText("");
            }
            else{
                flightname.setText(rs.getString("FlightName"));
                departure.setText(rs.getString("Departure"));
                arrival.setText(rs.getString("Arrival"));
                duration.setText(rs.getString("Duration"));
                
                java.util.Date da1 = new SimpleDateFormat("yyyy-MM-dd").parse(rs.getString("DateOfJourney"));
                dob.setDate(da1);
                //2023-02-12 => 12 feb 2023(dd-MMM-yyyy)
                
                fare.setText(rs.getString("Fare"));
                tickets.setText(rs.getString("Tickets"));
                total.setText(rs.getString("TotalFare"));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AbortJourney.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AbortJourney.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(AbortJourney.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

