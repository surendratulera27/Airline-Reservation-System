    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String FLIGHTID = flightid.getText();
            String FLIGHTNAME = flightname.getText();
            String DEPARTURE = departure.getText();
            String ARRIVAL = arrival.getText();
            String FARE = fare.getText();
            String DURATION = duration.getText();
            String SEATS = seats.getText();
            
            DateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            String DATE = date.format(dob.getDate());
            //20 feb. 2023 => 2023-02-20
            
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/airlinereservationsystem", "root", "TULera@123");
            pre = con.prepareStatement("insert into flight(FlightId, FlightName, Departure, Arrival, Fare, Date, Duration, Seats) values(?,?,?,?,?,?,?,?)");
            pre.setString(1, FLIGHTID);
            pre.setString(2, FLIGHTNAME);
            pre.setString(3, DEPARTURE);
            pre.setString(4, ARRIVAL);
            pre.setString(5, FARE);
            pre.setString(6, DATE);
            pre.setString(7, DURATION);
            pre.setString(8, SEATS);
            
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null, "Flight added successfully");
            
            AutoId();
            flightname.setText("");
            departure.setText("");
            arrival.setText("");
            fare.setText("");
            duration.setText("");
            seats.setText("");
            dob.setDate(null);
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

